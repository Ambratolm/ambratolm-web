//==============================================================================
// ■ Footer Directive
//------------------------------------------------------------------------------
//      <amb-footer></amb-footer>
//==============================================================================

//------------------------------------------------------------------------------
// ● Imports
//------------------------------------------------------------------------------
import angular from "angular";
//------------------------------------------------------------------------------
// ● Controller
//------------------------------------------------------------------------------
function footerDirectiveController() {
    let currentYear = new Date().getFullYear();
    this.copyright = `© Ambratolm 2000 - ${currentYear}`;
}
//------------------------------------------------------------------------------
// ● Factory
//------------------------------------------------------------------------------
function footerDirectiveFactory() {
    return {
        restrict: "E",
        controller: footerDirectiveController,
        controllerAs: "footer",
        template: require("./footer.directive.html")
    };
}
//------------------------------------------------------------------------------
// ● Module
//------------------------------------------------------------------------------
export default angular
    .module("footer.directive", [])
    .directive("ambFooter", footerDirectiveFactory).name;
