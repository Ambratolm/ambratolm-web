//==============================================================================
// ■ Collapse Directive
//------------------------------------------------------------------------------
//      This directive wraps bootstrap collapse functionality.
//------------------------------------------------------------------------------
//      <element amb-collapse="@selector"></element>
//==============================================================================

//------------------------------------------------------------------------------
// ● Imports
//------------------------------------------------------------------------------
import angular from "angular";
//------------------------------------------------------------------------------
// ● Link
//------------------------------------------------------------------------------
function collapseDirectiveLink(scope, element, attrs) {
	element.attr("data-target", attrs.ambCollapse);
	element.attr("data-toggle", "collapse");
	element.attr("aria-expanded", "false");
}
//------------------------------------------------------------------------------
// ● Factoy
//------------------------------------------------------------------------------
function collapseDirectiveFactory() {
	return {
		restrict: "A",
		link: collapseDirectiveLink
	};
}
//------------------------------------------------------------------------------
// ● Module
//------------------------------------------------------------------------------
export default angular
	.module("collapse.directive", [])
	.directive("ambCollapse", collapseDirectiveFactory).name;
